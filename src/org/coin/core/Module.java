package org.coin.core;

public class Module {
	public long lId;
	public String sName;
	public int iPosX;
	public int iPosY;
	
	
	public Module (
			long lId,
			String sName,
			int iPosX,
			int iPosY) 
	{
		this.lId = lId;
		this.sName = sName;
		this.iPosX = iPosX;
		this.iPosY = iPosY;
	}
	
}
